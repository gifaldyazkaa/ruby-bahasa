# Latihan 8 - If Statement, Else, Elseif

# Statemen If dipakai ketika kita menanyakan apakah ini benar? (Ini semacam pemeriksaan).

# Kita lihat contohnya.

angka = 10

if angka == 10

    puts "Angkanya 10"

elsif angka == 100

    puts "Angkanya 100"

else

    puts "Angkanya bukan 10 dan 100."

end

# Nah, cobalah untuk mengubah variabel angka dari 10 Jadi 100. Lalu Save.

