# Lengkapnya, Def adalah Definisi.

# Kita mau mendefinisikan kalau kita menyebut namanya (di projek ini saya pakai nama "jalan") dalam program, maka program akan melakukan hal yang sudah disiapkan kalian.

# Jadi ini (mungkin) sama seperti variabel. Kita menyimpan dulu perintah. Tapi, Ketika kita panggil namanya, maka Perintah akan Dilaksanakan.

# Parameter gimana pak?

