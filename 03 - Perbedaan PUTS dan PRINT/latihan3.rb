# Latihan 3 - Perbedaan PUTS dan PRINT di Ruby.

# Kalau pakai PUTS

var1 = "Halo"

var2 = "HeyHey"

puts "Ini Pakai PUTS : "

puts var1

puts var2

# Di Terminal, kamu akan melihat bahwa Halo dan HeyHey ada di baris Berbeda.

# Sedangkan kalau pakai PRINT

puts ""

print "Ini pakai PRINT : "

print var1

print var2

puts ""

# Nahh kan, kalau pakai PRINT, Halo dan HeyHey akan bersatu di baris yang sama.

# Jadi, Itulah perbedaannya. Pakailah kedua sintaks ini sesuai kebutuhan.

